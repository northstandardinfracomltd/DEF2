/**
 * Returns the current active language stored in localStorage or defaults to 'Français'.
 */
export function getLanguage(): string {
  if (typeof window === 'undefined') return 'Français';
  const stored = localStorage.getItem('defib_lang') || 'Français';
  const lower = stored.toLowerCase();
  if (lower.startsWith('fr') || lower.includes('français') || lower.includes('francais')) return 'Français';
  if (lower.startsWith('en') || lower.includes('english')) return 'English';
  if (lower.startsWith('de') || lower.includes('deutsch')) return 'Deutsch';
  if (lower.startsWith('pt') || lower.includes('português') || lower.includes('portugues')) return 'Português';
  if (lower.startsWith('es') || lower.includes('español') || lower.includes('espanol')) return 'Español';
  return 'Français';
}

/**
 * Sets the current active language and updates localStorage.
 */
export function setLanguage(lang: string): void {
  if (typeof window !== 'undefined') {
    localStorage.setItem('defib_lang', lang);
    // Dispatch custom event to let other components know the language changed
    window.dispatchEvent(new Event('defib_lang_changed'));
  }
}

/**
 * Translates a French string to the active language if it is NOT 'Français'.
 * Fallbacks to English, and then to original French if translation is not found.
 */
export function t(frenchText: string | undefined | null): string {
  if (!frenchText) return '';
  
  const currentLang = getLanguage();
  // If the language is French, return the original text
  if (currentLang === 'Français') {
    return frenchText;
  }

  let trimmed = frenchText.trim();

  // Try exact match on the full trimmed string first (with punctuation/dots intact)
  const fullDict = dictionaries[currentLang] || dictionaries['English'] || {};
  if (fullDict[trimmed]) {
    return fullDict[trimmed];
  } else {
    const lowerTrimmed = trimmed.toLowerCase();
    const foundFullKey = Object.keys(fullDict).find(
      (key) => key.toLowerCase() === lowerTrimmed
    );
    if (foundFullKey) {
      return fullDict[foundFullKey];
    } else if (currentLang !== 'English') {
      const engFullDict = dictionaries['English'] || {};
      if (engFullDict[trimmed]) {
        return engFullDict[trimmed];
      }
      const foundEngFullKey = Object.keys(engFullDict).find(
        (key) => key.toLowerCase() === lowerTrimmed
      );
      if (foundEngFullKey) {
        return engFullDict[foundEngFullKey];
      }
    }
  }

  // Detect suffixes like '.', ':', '*', ' *', etc.
  let suffix = '';
  
  if (trimmed.endsWith(':')) {
    suffix = ':';
    trimmed = trimmed.slice(0, -1).trim();
  } else if (trimmed.endsWith('.')) {
    suffix = '.';
    trimmed = trimmed.slice(0, -1).trim();
  } else if (trimmed.endsWith('*')) {
    suffix = '*';
    trimmed = trimmed.slice(0, -1).trim();
  }

  // 1. Handle parenthesized numbers at the end like: "Extincteur (12)" or "Clients (5)"
  const parenCountMatch = trimmed.match(/^(.*?)\s*\((\d+)\)\s*$/);
  if (parenCountMatch) {
    const baseText = parenCountMatch[1];
    const countVal = parenCountMatch[2];
    const translatedBase = t(baseText);
    return `${translatedBase} (${countVal})${suffix}`;
  }

  // 2. Handle numbered prefixes like: "9 — Catégories" or "1 — Identifiant"
  const numberPrefixMatch = trimmed.match(/^(\d+\s*[-—–\.]\s*)(.*)$/);
  if (numberPrefixMatch) {
    const prefixVal = numberPrefixMatch[1];
    const restText = numberPrefixMatch[2];
    const translatedRest = t(restText);
    return `${prefixVal}${translatedRest}${suffix}`;
  }

  const dict = dictionaries[currentLang] || dictionaries['English'] || {};

  // Exact match lookup on the trimmed base
  let translated = '';
  if (dict[trimmed]) {
    translated = dict[trimmed];
  } else {
    const lower = trimmed.toLowerCase();
    const foundKey = Object.keys(dict).find(
      (key) => key.toLowerCase() === lower
    );
    if (foundKey) {
      translated = dict[foundKey];
    } else if (currentLang !== 'English') {
      // English fallback
      const engDict = dictionaries['English'] || {};
      if (engDict[trimmed]) {
        translated = engDict[trimmed];
      } else {
        const engKey = Object.keys(engDict).find(
          (key) => key.toLowerCase() === lower
        );
        if (engKey) {
          translated = engDict[engKey];
        }
      }
    }
  }

  // If a translation was found, reconstruct with original suffix
  if (translated) {
    if (suffix === ':') {
      return translated + ':';
    }
    if (suffix === '*') {
      return translated + ' *';
    }
    return translated + suffix;
  }

  // Fallback prefix handlers
  if (trimmed.startsWith('Nouveau ')) {
    const rest = trimmed.slice(8);
    const translatedRest = t(rest);
    if (currentLang === 'Deutsch') return `Neu ${translatedRest}${suffix}`;
    if (currentLang === 'Português') return `Novo ${translatedRest}${suffix}`;
    if (currentLang === 'Español') return `Nuevo ${translatedRest}${suffix}`;
    return `New ${translatedRest}${suffix}`;
  }
  if (trimmed.startsWith('Nouveau')) {
    const rest = trimmed.slice(7);
    const translatedRest = t(rest);
    if (currentLang === 'Deutsch') return `Neu${translatedRest}${suffix}`;
    if (currentLang === 'Português') return `Novo${translatedRest}${suffix}`;
    if (currentLang === 'Español') return `Nuevo${translatedRest}${suffix}`;
    return `New${translatedRest}${suffix}`;
  }
  if (trimmed.startsWith('Modification ')) {
    const rest = trimmed.slice(13);
    const translatedRest = t(rest);
    if (currentLang === 'Deutsch') return `Bearbeiten ${translatedRest}${suffix}`;
    if (currentLang === 'Português') return `Editar ${translatedRest}${suffix}`;
    if (currentLang === 'Español') return `Editar ${translatedRest}${suffix}`;
    return `Edit ${translatedRest}${suffix}`;
  }
  if (trimmed.startsWith('Supprimer ')) {
    const rest = trimmed.slice(10);
    const translatedRest = t(rest);
    if (currentLang === 'Deutsch') return `Löschen ${translatedRest}${suffix}`;
    if (currentLang === 'Português') return `Eliminar ${translatedRest}${suffix}`;
    if (currentLang === 'Español') return `Eliminar ${translatedRest}${suffix}`;
    return `Delete ${translatedRest}${suffix}`;
  }
  if (trimmed.startsWith('Total ')) {
    const rest = trimmed.slice(6);
    const translatedRest = t(rest);
    if (currentLang === 'Deutsch') return `Gesamt ${translatedRest}${suffix}`;
    if (currentLang === 'Português') return `Total ${translatedRest}${suffix}`;
    if (currentLang === 'Español') return `Total ${translatedRest}${suffix}`;
    return `Total ${translatedRest}${suffix}`;
  }

  return frenchText;
}

// Keep track of DOM translator listener
let observerInstance: MutationObserver | null = null;
let isTranslating = false;

/**
 * Translates a single DOM Node recursively.
 */
function translateDOMNode(el: Node) {
  const currentLang = getLanguage();
  if (currentLang === 'Français') return;

  // 1. Walk through all text nodes
  const walker = document.createTreeWalker(el, NodeFilter.SHOW_TEXT);
  let textNode = walker.nextNode();
  while (textNode) {
    const originalText = textNode.nodeValue || '';
    const trimmed = originalText.trim();
    if (trimmed && trimmed.length > 1) {
      const parent = textNode.parentElement;
      const parentTagName = parent ? parent.tagName.toLowerCase() : '';
      if (
        parentTagName !== 'script' &&
        parentTagName !== 'style' &&
        parentTagName !== 'textarea' &&
        !parent?.hasAttribute('contenteditable')
      ) {
        const translated = t(trimmed);
        if (translated !== trimmed) {
          const withSpacing = originalText.replace(trimmed, translated);
          textNode.nodeValue = withSpacing;
        }
      }
    }
    textNode = walker.nextNode();
  }

  // 2. Translate placeholders, titles, value attributes for inputs and option texts
  if (el.nodeType === Node.ELEMENT_NODE) {
    const element = el as Element;
    
    // Translate the element itself if it matches tags
    const tagName = element.tagName.toLowerCase();
    if (tagName === 'input' || tagName === 'textarea') {
      const input = element as HTMLInputElement | HTMLTextAreaElement;
      if (input.placeholder) {
        const tr = t(input.placeholder);
        if (tr !== input.placeholder) {
          input.placeholder = tr;
        }
      }
      if (input.type === 'button' || input.type === 'submit') {
        const tr = t(input.value);
        if (tr !== input.value) {
          input.value = tr;
        }
      }
    }

    if (tagName === 'option') {
      const option = element as HTMLOptionElement;
      const tr = t(option.text);
      if (tr !== option.text) {
        option.text = tr;
      }
    }

    if (tagName === 'button') {
      const button = element as HTMLButtonElement;
      if (button.textContent) {
        const trimmed = button.textContent.trim();
        const tr = t(trimmed);
        if (tr !== trimmed) {
          button.textContent = button.textContent.replace(trimmed, tr);
        }
      }
    }

    // Now query inside the element
    const childNodes = element.querySelectorAll('input, textarea, option, button');
    childNodes.forEach((node) => {
      const tag = node.tagName.toLowerCase();
      if (tag === 'input' || tag === 'textarea') {
        const input = node as HTMLInputElement | HTMLTextAreaElement;
        if (input.placeholder) {
          const tr = t(input.placeholder);
          if (tr !== input.placeholder) {
            input.placeholder = tr;
          }
        }
        if (input.type === 'button' || input.type === 'submit') {
          const tr = t(input.value);
          if (tr !== input.value) {
            input.value = tr;
          }
        }
      }

      if (tag === 'option') {
        const option = node as HTMLOptionElement;
        const tr = t(option.text);
        if (tr !== option.text) {
          option.text = tr;
        }
      }

      if (tag === 'button') {
        const button = node as HTMLButtonElement;
        if (button.textContent) {
          const trimmed = button.textContent.trim();
          const tr = t(trimmed);
          if (tr !== trimmed) {
            button.textContent = button.textContent.replace(trimmed, tr);
          }
        }
      }
    });
  }
}

/**
 * Starts real-time automated DOM-level translation to cover all tabs, forms, fields,
 * option elements, and radio button options dynamically without modifying component hierarchies.
 */
export function startDOMTranslation(): void {
  if (typeof window === 'undefined') return;

  if (observerInstance) {
    observerInstance.disconnect();
    observerInstance = null;
  }

  let animationFrameId: number | null = null;

  const runTranslation = () => {
    if (isTranslating) return;
    const currentLang = getLanguage();
    if (currentLang === 'Français') return;

    if (animationFrameId !== null) {
      cancelAnimationFrame(animationFrameId);
    }

    animationFrameId = requestAnimationFrame(() => {
      if (isTranslating) return;
      isTranslating = true;

      if (observerInstance) {
        observerInstance.disconnect();
      }

      try {
        translateDOMNode(document.body);
      } catch (err) {
        console.error('Error translating DOM:', err);
      } finally {
        isTranslating = false;
        if (observerInstance) {
          observerInstance.observe(document.body, {
            childList: true,
            subtree: true,
            characterData: true,
            attributes: true,
            attributeFilter: ['placeholder', 'value', 'title']
          });
        }
      }
    });
  };

  observerInstance = new MutationObserver((mutations) => {
    runTranslation();
  });

  observerInstance.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['placeholder', 'value', 'title']
  });

  // Run initial translation
  setTimeout(runTranslation, 100);

  // Re-run whenever custom language changed event is received
  window.addEventListener('defib_lang_changed', runTranslation);
}
